# String is a bunch of characters

a = 'Selenium'
print(a)

b = "Python"
print(b)

# Both a and b are strings only

print(type(a))  # <class 'str'>

print(type(b)) # <class 'str'>

# double or single quote doesnt matter

# Len will always start from 1

print(len(a))
print(len(b))

# Upper and Lower case

print(a.lower())
print(b.lower())
print(b.upper())
print(a.upper())





