"""Loops
Loop is Repeat a block of code multiple times

Block of code can be anything - something we want to repeat , execute multiple times

Diff types of Loops - for loop, while loop, do while loop,

Used to iterate over a sequence (like list, tuple, dictionary, string or range)

Used to

range() - its inbuilt() function used to generate a sequence of numbers.
commonly used in loops

Syntax: range(start, stop, step)

start - The starting value (inclusive)  - Default - 0
stop - The ending value (Exclusive) - Default - "-1"
step - The diff between each  number  - default - 1
"""

