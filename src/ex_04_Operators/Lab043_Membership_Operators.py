""" Membership Operators
Used to test if a value present in a sequence(like list, string, tuple, etc)

in -- True if value found
not in  -- True if value not found

example  a in apple  -- True

b not in apple  -- True

"""
result = 'a' in 'Automation'
print(result)

result = 'a'  in 'Selenium'
print(result)

result = 'a' not in 'Selenium'
print(result)





